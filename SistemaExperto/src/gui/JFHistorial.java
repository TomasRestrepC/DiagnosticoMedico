    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        String nombre = txtNombrePaciente.getText().trim();
        
        if (nombre.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, ingrese el nombre del paciente.", "Advertencia", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        // Llamar al m√©todo del controlador
        String historial = gestor.consultarHistorialPaciente(nombre);
        
        // Mostrar el resultado en el JTextArea
        txtResultados.setText(historial);
        txtResultados.setCaretPosition(0); // Volver al inicio del texto
    }//GEN-LAST:event_btnConsultarActionPerformed

