    private void cargarCheckboxesSintomas() {
        List<String> sintomas = controlador.obtenerTodosLosSintomas();

        panelSintomas.setLayout(new GridLayout(0, 2));

        for (String s : sintomas) {
            JCheckBox check = new JCheckBox(s);
            listaChecks.add(check);
            panelSintomas.add(check);
        }

        panelSintomas.revalidate();
        panelSintomas.repaint();
    }



