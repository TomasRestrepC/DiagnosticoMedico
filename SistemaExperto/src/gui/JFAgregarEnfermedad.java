    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        String nombre = jTextFieldNombre.getText().trim();
        String categoria = jComboBox1.getSelectedItem().toString();
        String recomendacion = jTextFieldRecomendacion.getText().trim();

        if (nombre.isEmpty() || recomendacion.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Todos los campos son obligatorios.");
        return;
        }

        List<String> sintomasSeleccionados = new ArrayList<>();
        for (JCheckBox c : listaChecks) {
            if (c.isSelected()) sintomasSeleccionados.add(c.getText());
        }

        if (sintomasSeleccionados.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Selecciona al menos un s√≠ntoma.");
            return;
        }

        int id = controlador2.agregarEnfermedad(nombre, categoria, recomendacion);

        if (id != -1) {
            controlador.agregarSintomas(id, sintomasSeleccionados);
            JOptionPane.showMessageDialog(this, "Enfermedad agregada correctamente.");
        } else {
            JOptionPane.showMessageDialog(this, "Error al agregar enfermedad.");
        }  
    }//GEN-LAST:event_btnGuardarActionPerformed

