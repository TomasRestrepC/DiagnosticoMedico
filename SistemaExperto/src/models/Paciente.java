package models;

/**
 *
 * @author smuel
 */
public class Paciente {
    private int id;
    private String nombre;
    private int edad;

    public Paciente(int id, String nombre, int edad) {
        this.id = id;
        this.nombre = nombre;
        this.edad = edad;
    }

    public int getId() {
        return id;
    }

    public String getNombre() {
        return nombre;
    }

    public int getEdad() {
        return edad;
    }
}
